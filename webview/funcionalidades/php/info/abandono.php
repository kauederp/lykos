    <p>A sociedade vem estabelecendo convivência com os animais a muito tempo,
apesar disso, pode-se notar os numerosos casos de maus-tratos cometidos pelos
seres humanos, como: abandono, agressões, tráfico, zoofilia, caça, testes de
cosméticos, entre outras atrocidades.</p>

<p>Pode-se justificar esse senso de superioridade dos humanos perante aos outros
seres vivos pela história, pois a bíblia, o livro mais vendido do mundo, defende esse
pensamento:</p>

<p class="fw-bold">“Então disse Deus: “Façamos o homem à nossa imagem, conforme a nossa
semelhança. Domine ele sobre os peixes do mar, sobre as aves do céu,
sobre os grandes animais de toda a terra e sobre todos os pequenos
animais que se movem rente ao chão”(Gênesis 1:26).</p>

<p>Obviamente que este livro não dita o comportamento de toda a população
mundial, mas ao observar a cultura, costumes e obras do ocidente, pode se
constatar grande influência do cristianismo.
</p>
<p>
Atualmente a igualdade do ecossistema vem sendo cada vez mais discutida e
defendida, mas essas informações acabam chegando distorcidas para os grupos
que convivem nesta bolha de proteção ambiental.
</p>

<p>Por conta disso, o abandono tem sido um problema grave nas últimas
décadas, causando danos à saúde e ao bem-estar animal, bem como na ecologia e
na economia do país. Os animais são seres que demandam cuidado e amor, e
assim como muitos são cuidados e amados por seus tutores, outros são
descartados como lixo.</p>

<p>Segundo a Organização Mundial da Saúde (OMS) estima-se que só no Brasil
existem mais de 30 milhões de animais abandonados, entre eles, 10 milhões são
gatos e 20 milhões são cães. Só em Porto Alegre, Rio Grande do Sul (Região
Metropolitana), estima-se que existam 500 mil cães e gatos em situação de
abandono.</p>

<p>Os descartes de animais acontecem em qualquer lugar, como parques,
praças, estradas, portas de pet shops, hospitais veterinários e principalmente nas
ONG 's. A população não tem o discernimento de responsabilidade ao adotar um
animal, e a primeira atitude que é julgada como errada aos olhos do tutor, se
torna o motivo para o abandono e agressão.
Normalmente, os animais com mais risco de abandono são os que
apresentam problemas de comportamento e/ou deficiência, ou seja, animais que
demandam mais atenção e cuidado.</p>

<p>Os motivos para o abandono são diversos, como: Mudar de endereço, custo
alto demais, não querer a cria do animal (em caso de fêmeas), animal velho demais,
sujeira causada pelo animal (pelos, poeira, defecar e urinar em locais considerados
impróprios), falta de tempo, não compatibilidade entre os outros animais de
estimação, entre outros.</p>

<p>O abandono de animais é considerado crime perante a legislação brasileira, a
Lei 1095/2019 ou Lei Sansão, decreta que o indivíduo que cometer maus-tratos
contra cães e gatos, terá pena de dois a cinco anos de prisão com multa e não
poderá ter a tutela de animais no futuro. Este foi um grande passo para os
defensores do movimento, mas não resolve a maior parte do problema. Pode-se
constatar que este crime não é visto pela sociedade, pois as pessoas não são
julgadas por abandonarem e maltratarem os animais, como uma lei cega aos olhos
da crueldade.</p>

<p>Desse modo, existem diferentes caminhos para reeducar o olhar da
população para com os animais, como: Informações diárias e confiáveis sobre o
reino animal e o meio ambiente, educação de qualidade voltada para o cuidado e
reconstrução do ambiente e dos seres que nele habitam, reestruturação de
pensamento pela literatura, cinema e obras de arte, bem como incentivar a adoção
consciente aos tutores, e manter leis firmes para aqueles que agredirem,
abandonarem e/ou maltratarem os animais.</p>
